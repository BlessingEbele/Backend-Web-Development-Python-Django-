
'''
these operators help Python compare values.
Operator    Meaning
==          equal to
!=          not equal to
>           greater than
<           less than
>=          greater or equal
<=          less or equal

'''
#Example:
print(10 > 5)  # True
print(3 == 4)  # False
print(3 != 1) #True 
print(4 > 7) #False
print(9 < 6) #False
print(9 >= 8) # True
print(4 <= 2) # False


